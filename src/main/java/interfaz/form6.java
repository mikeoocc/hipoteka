    private void sigTxtMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_sigTxtMouseClicked
        
        Boolean cancelar = false;
        
        if(sigB9.getBackground().equals(colorP)){
            Main.getIntance().getUsuarioActivo().setAporteMaximo(sigTxt9.getText());
        }
        else if(sigB11.getBackground().equals(colorP)){
            Main.getIntance().getUsuarioActivo().setAporteMaximo(sigTxt11.getText());
        }
        else if(sigB12.getBackground().equals(colorP)){
            Main.getIntance().getUsuarioActivo().setAporteMaximo(sigTxt12.getText());
        }
        else{
            cancelar = true;
        }
      
        if(cancelar == true){
            JOptionPane.showMessageDialog(this, "Seleccione una opcion", "Error", JOptionPane.ERROR_MESSAGE);
        }
        else{
            BaseDatos.getInstance().rellenarCampos();
            this.dispose();

            vPCliente vP = new vPCliente();
            vP.setVisible(true);
        }
        
    }//GEN-LAST:event_sigTxtMouseClicked

